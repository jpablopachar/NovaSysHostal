    private void tablaListadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListadoMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) { // Si el usuario hace doble clic
            int fila = tablaListado.getSelectedRow(); // Recoge la fila seleccionado en la tablaListado
            String codigo, valor; // Almacena el codigo y el valor

            codigo = tablaListado.getValueAt(fila, 0).toString(); // Envia el idCliente
            valor = tablaListado.getValueAt(fila, 1).toString() + " " + tablaListado.getValueAt(fila, 2).toString(); // Envia el nombre y el apellido

            VistaReserva.txtIdCliente.setText(codigo); // Envia a txtIdCliente el idCliente
            VistaReserva.txtCliente.setText(valor); // Envia a txtNombreCliente el nombre del cliente
            this.dispose(); // Al final se cierra la tabla
        }
    }//GEN-LAST:event_tablaListadoMousePressed

