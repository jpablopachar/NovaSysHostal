    private void tablaListadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListadoMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) { // El usuario ha hecho doble clic
            int fila = tablaListado.getSelectedRow(); // Obtiene el indice de la fila que el usuario hizo clic
            
            String codigo, valor1, valor2;
            
            codigo = tablaListado.getValueAt(fila, 0).toString();
            valor1 = tablaListado.getValueAt(fila, 1).toString();
            valor2 = tablaListado.getValueAt(fila, 5).toString();
            
            // Enviamos los valores a la VistaConsumo
            VistaConsumo.txtIdServicio.setText(codigo);
            VistaConsumo.txtServicio.setText(valor1);
            VistaConsumo.txtPrecioVenta.setText(valor2);
            
            this.setVisible(false);
        }
    }//GEN-LAST:event_tablaListadoMousePressed

