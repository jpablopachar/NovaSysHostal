    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        // Evalua que mi txtIdConsumo no est√© vac√≠a
        if (!txtIdConsumo.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¬øEst√°s seguro de eliminar el consumo?","Confirmar",2);

            // Si el usuario confirma se procede a la eliminaci√≥n
            if (confirmacion == 0) {
                LogicaConsumo logicaConsumo = new LogicaConsumo();
                Consumo consumo = new Consumo();

                consumo.setIdConsumo(Integer.parseInt(txtIdConsumo.getText()));
                logicaConsumo.eliminar(consumo);
                mostrar(idReserva); // Muestra solo el consumo de la reserva
                inHabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

